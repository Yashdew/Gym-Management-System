    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here: Update
        
        String id=jTextField1.getText();
        String name=jTextFieldName.getText();
        String mobilenumber=jTextFieldMobile.getText();
        String email=jTextFieldemail.getText();
        //String gender=jTextFieldgender.getText();
        String fathername =jTextFieldfather.getText();
        String mothername=jTextFieldmother.getText();
        //String gymtime=jTextFieldgym.getText();
        String aadharnumber=jTextFieldaadharcard.getText();
        String age=jTextFieldage.getText();
        String amount=jTextFieldamout.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps=con.prepareStatement("update member set name=?,mobilenumber=?,email=?,fathername=?,mothername=?,aadharnumber=?,age=?,amount=? where id=?");
            ps.setString(1, name);
            ps.setString(2, mobilenumber);
            ps.setString(3, email);
            ps.setString(4, fathername);
            ps.setString(5, mothername);
            ps.setString(6, aadharnumber);
            ps.setString(7, age);
            ps.setString(8, amount);
            ps.setString(9, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Successfully Updated");
            setVisible(false);
            new UpdateDeleteMember().setVisible(true);
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

