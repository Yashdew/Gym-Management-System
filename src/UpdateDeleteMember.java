    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here: Search
        int checkid=0; 
        String id =jTextField1.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from member where id='"+id+"'");
            while(rs.next())
            {
                checkid=1;
                jTextField1.setEditable(false);
                jTextFieldName.setText(rs.getString(2));
                jTextFieldMobile.setText(rs.getString(3));
                jTextFieldemail.setText(rs.getString(4));
                jTextFieldgender.setText(rs.getString(5));
                jTextFieldgender.setEditable(false);
                jTextFieldfather.setText(rs.getString(6));
                jTextFieldmother.setText(rs.getString(7));
                jTextFieldgym.setText(rs.getString(8));
                jTextFieldgym.setEditable(false);
                jTextFieldaadharcard.setText(rs.getString(9));
                jTextFieldage.setText(rs.getString(10));
                jTextFieldamout.setText(rs.getString(11));
            }
            if (checkid==0)
            {
                JOptionPane.showMessageDialog(null, "Member ID doesn't exist");
                
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

